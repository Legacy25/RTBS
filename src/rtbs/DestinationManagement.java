    private void initiateDB()
    {
        JDBConnection jdbc=new JDBConnection();
        jdbc.connect();
        ds=jdbc.getDestination();
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        for(int i=0;i<ds.length;i++)
        {
            jComboBox1.addItem(ds[i].getStation()+", "+ds[i].getCity());
            jComboBox2.addItem(ds[i].getStation()+", "+ds[i].getCity());
        }
    }
    
    private boolean checkCreateValid()
    {
        if(jTextField1.getText().equals("") ||
                jTextField2.getText().equals("") ||
                    jTextField3.getText().equals(""))
        {
            return false;
        }
        else
        {
            return true;
        }
    }
    
    private boolean checkUpdateValid()
    {
        if(jTextField5.getText().equals("") ||
                jTextField6.getText().equals(""))
        {
            return false;
        }
        else
        {
            return true;
        }
    }
