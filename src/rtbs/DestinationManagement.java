    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!checkCreateValid())
        {
            JOptionPane.showMessageDialog(null, "Add all fields", "Add all fields", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        JDBConnection jdbc=new JDBConnection();
        jdbc.connect();
        Destination d=new Destination();
        d.setID(Integer.parseInt(jTextField1.getText()));
        d.setCity(jTextField2.getText());
        d.setStation(jTextField3.getText());
        jdbc.addDestination(d);
        initiateDB();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!checkUpdateValid())
        {
            JOptionPane.showMessageDialog(null, "Add all fields", "Add all fields", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        JDBConnection jdbc=new JDBConnection();
        jdbc.connect();
        Destination d=new Destination();
        d.setID(ds[jCBox1].getID());
        d.setCity(jTextField5.getText());
        d.setStation(jTextField6.getText());
        jdbc.updateDestination(d);
        initiateDB();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        JDBConnection jdbc=new JDBConnection();
        jdbc.connect();
        if(!jdbc.trainExists(ds[jCBox2].getID()))
        {
            jdbc.deleteDestination(ds[jCBox2].getID());
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Cannot remove destination. Trains are associated with it.", "Invalid Operation", JOptionPane.ERROR_MESSAGE);
        }
        initiateDB();
    }//GEN-LAST:event_jButton3ActionPerformed

